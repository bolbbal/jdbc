package javaBasic;

public class Java01_Sysout {
	//public : 접근제한자 중 하나이며 Java01_Sysout 클래스는 누구든지 접근이 가능하다.
	//class 객체를 생성하기 위한 설계도

	public static void main(String[] args) {
		//public 이 메소드와 같이 사용하면 누구든지 main()에 접근할 수 있다.
		//static 객체생성 없이 main()메서드를 사용할 수 있다.
		//void 리턴해주는 값이 없다.
		//String[] args 외부에서 전달값을 저장하는 매개변수
		
		System.out.println("Hello Java!");
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
		System.out.println("****");
		
	}

}
